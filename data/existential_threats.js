"We even have a label for our era in the grand scheme of things - calling it the 'Anthropocene'.\n\
\n\
The Anthropocene is an informal geologic chronological term for the proposed epoch that began when human activities had a significant global impact on the Earth's ecosystems [ http://en.wikipedia.org/wiki/Anthropocene ]. Our era is characterized as a time in which our actions have changed Earth’s diversity of life and geology enough to be visible in the future geological record.\n\
\n\
We have a planet straddling civilization that uses two-fifths of the arable land for food crops, that controls the flow of most rivers, that has cities so dense that we can see the outlines of the continents from space at night. We’ve chemically altered the balance of constituent elements in the atmosphere and may be altering the planet’s surface temperature.\n\
\n\
The other major epochs are also divided by extinction events: the Paleozoic, Mesozoic and Cenozoic Extinction Events\n\
\n\
The Earth has existed for 4.54 billion years and at some point in that first four billion years life starts to exist. Up until then the Earth had a destiny not unlike the moon or Mars - beautiful yet empty and devoid.\n\
\n\
How life came to be, how life emerged and blossomed, the challenges life faced and where it is today has been painstakingly revealed through field study, interrogation of natural landscapes, studying fossil remains and constant debate. The research and monumental labor is the work of thousands of scientists over multiple generations of human life spans. This debate is ongoing and far from resolved.\n\
\n\
We divide the eras of the evolution of life on our home planet into three major parts: the Paleozoic, the Mesozoic and the Cenozoic. These are greek words that simply mean what they say. Paleo is from the Greek palaios and means ‘ancient.’. Meso means ‘intermediate’ and is from the Greek zōion meaning ‘animal’ (where Mesozoic is referring to the appearance of the first mammals). Ceno is from the Greek kainos ‘new’ + zōion ‘animal’.\n\
\n\
The Paleozoic begins with what is referred to as the Cambrian explosion. A sudden increase in the diversity of life. We also see life move onto land and we see the evolution of the first dinosaurs. There are three major extinction events in the Paleozoic and the end of the Paleozoic is marked by the Permian-Triassic extinction event which kills off 90% of all diversity of life.\n\
\n\
The Mesozoic begins slowly with life on land taking 30 million years to recover. Ultimately the Mesozoic is dominated by dinosaurs. The continents break up from Pangea and begin to drift towards where we would recognize them today. There are two major extinction events and the Mesozoic ends with the Cretaceous-Paleogene extinction event that kills off 70% of all diversity of life.\n\
\n\
The Cenozoic (our era) is characterized by being cooler than the previous eras and by the emergence of mammals. We may be beginning to enter the sixth major extinction event with a 40% die off of diversity of life so far.\n\
\n\
From http://en.wikipedia.org/wiki/Extinction_event - Jack Sepkoski and David M. Raup identify five of the largest mass extinctions:\n\
\n\
Ordovician–Silurian extinction events. Two events occurred that killed off 27% of all families, 57% of all genera and 60% to 70% of all species.\n\
Late Devonian extinction. A prolonged series of extinctions eliminated about 19% of all families, 50% of all genera and 70% of all species.\n\
Permian–Triassic extinction event. Earth's largest extinction killed 57% of all families, 83% of all genera and 90% to 96% of all species.\n\
Triassic–Jurassic extinction event. About 23% of all families, 48% of all genera (20% of marine families and 55% of marine genera) and 70% to 75% of all species went extinct. Most non-dinosaurian archosaurs, most therapsids, and most of the large amphibians were eliminated, leaving dinosaurs with little terrestrial competition.\n\
Cretaceous–Paleogene extinction event. About 17% of all families, 50% of all genera and 75% of all species became extinct. In the seas it reduced the percentage of sessile animals to about 33%. Mammals and birds emerged as dominant land vertebrates in the age of new life."
